// ─── Lookup function ───────────────────────────────────────────────────────────
const DICTS = { zh: ZH, en: EN } as const

export function t(
  key: TranslationKey,
  lang: 'zh' | 'en',
  vars?: Record<string, string | number>,
): string {
  let str: string = DICTS[lang][key] ?? DICTS.zh[key] ?? key
  if (vars) {
    for (const [k, v] of Object.entries(vars)) {
      str = str.replaceAll(`{${k}}`, String(v))
    }
  }
  return str
}
